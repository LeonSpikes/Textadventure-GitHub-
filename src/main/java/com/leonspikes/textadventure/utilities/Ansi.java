package com.leonspikes.textadventure.utilities;

/**
 * In this class are all constants of color settings.
 *
 * @author Leon Spikes
 */
public class Ansi {

    /**
     * RESET means it resets all test color and backgrounds in console.
     */
    public static final String RESET = "\u001B[0m";

    /**
     * BOLD makes text bolder.
     */
    public static final String BOLD = "\u001B[1m";

    /**
     * Text color BLACK in console.
     */
    public static final String BLACK = "\u001B[30m";

    /**
     * Text color RED in console.
     */
    public static final String RED = "\u001B[31m";

    /**
     * Text color GREEN in console.
     */
    public static final String GREEN = "\u001B[32m";

    /**
     * Text color YELLOW in console.
     */
    public static final String YELLOW = "\u001B[33m";

    /**
     * Text color BLUE in console.
     */
    public static final String BLUE = "\u001B[34m";

    /**
     * Text color PURPLE in console.
     */
    public static final String PURPLE = "\u001B[35m";

    /**
     * Text color CYAN in console.
     */
    public static final String CYAN = "\u001B[36m";

    /**
     * Text color WHITE in console.
     */
    public static final String WHITE = "\u001B[37m";

    /**
     * Background color BLACK in console.
     */
    public static final String BLACK_BG = "\u001B[40m";

    /**
     * Background color RED in console.
     */
    public static final String RED_BG = "\u001B[41m";

    /**
     * Background color GREEN in console.
     */
    public static final String GREEN_BG = "\u001B[42m";

    /**
     * Background color YELLOW in console.
     */
    public static final String YELLOW_BG = "\u001B[43m";

    /**
     * Background color BLUE in console.
     */
    public static final String BLUE_BG = "\u001B[44m";

    /**
     * Background color PURPLE in console.
     */
    public static final String PURPLE_BG = "\u001B[45m";

    /**
     * Background color CYAN in console.
     */
    public static final String CYAN_BG = "\u001B[46m";

    /**
     * Background color WHITE in console.
     */
    public static final String WHITE_BG = "\u001B[47m";

}
